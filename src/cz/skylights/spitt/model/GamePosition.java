package cz.skylights.spitt.model;

public class GamePosition {
	
	private Integer epizodePos;
	private Integer levelPos;
	private Long scorePos;
	
	public Integer getEpizodePos() {
		return epizodePos;
	}
	public void setEpizodePos(Integer epizodePos) {
		this.epizodePos = epizodePos;
	}
	public Integer getLevelPos() {
		return levelPos;
	}
	public void setLevelPos(Integer levelPos) {
		this.levelPos = levelPos;
	}
	public Long getScorePos() {
		return scorePos;
	}
	public void setScorePos(Long scorePos) {
		this.scorePos = scorePos;
	}
}
